    private void txtCoor1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCoor1KeyTyped
        char c = evt.getKeyChar();
        if (!((c >= '0') && (c <= '9') || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE) ) ) {
            getToolkit().beep();
            evt.consume();
        }
        else{
            if (txtCoor1.getText().length()+1 > 2) {
                getToolkit().beep();
                evt.consume();
            }
        }
    }//GEN-LAST:event_txtCoor1KeyTyped

