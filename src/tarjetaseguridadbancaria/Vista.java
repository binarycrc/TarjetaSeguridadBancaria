    //metodo del boton probar 
    private void btnProbarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProbarActionPerformed
        //aumentamos los intentos uno por uno
        cantidadIntentos=cantidadIntentos+1;
        lbIntentos.setText("Tiene "+ String.valueOf(3-cantidadIntentos) +" de 3 intentos.");
        
        //variable con el indice de cliente
        int indiceCliente = jTableCliente.getSelectedRow();       
        //buscamos el objeto persona en la lista
        Persona persona = listaPersona.get(indiceCliente);
        //buscamos el objeto cliente en el objeto persona
        Cliente cliente = persona.getCliente();
        String strIntento = "";

        //evaluamos los campos ingresados
        if (Integer.parseInt(txtCoor1.getText()) != Integer.parseInt(cliente.getTarjetaCelda(1))){
            strIntento = strIntento+labCoor1.getText()+" (x) - ";
            txtCoor1.setForeground(Color.red);
        } 
        else{
            strIntento = strIntento+labCoor1.getText()+" - ";
            txtCoor1.setForeground(Color.BLACK);
        }
        if (Integer.parseInt(txtCoor2.getText())!=Integer.parseInt(cliente.getTarjetaCelda(3))){
            strIntento = strIntento+labCoor2.getText()+" (x) - ";
            txtCoor2.setForeground(Color.red);
        } 
        else{
            strIntento = strIntento+labCoor2.getText()+" - ";
            txtCoor2.setForeground(Color.BLACK);
        }
        if (Integer.parseInt(txtCoor3.getText())!=Integer.parseInt(cliente.getTarjetaCelda(5))){
            strIntento = strIntento+labCoor3.getText()+" (x)";
            txtCoor3.setForeground(Color.red);
        } 
        else{
            strIntento = strIntento+labCoor3.getText();
            txtCoor3.setForeground(Color.BLACK);
        }
        //si el intento tiene errores lo agregamos a la tabla
        //de intentos fallidos
        if (strIntento.contains("(x)")) {
            DefaultTableModel modeloIntentos = (DefaultTableModel) jTableIntentos.getModel();
            modeloIntentos.addRow(new Object[]{
                persona.getIdentificacion(),
                persona.getNombre() + " " + persona.getApellido(),
                strIntento
            });
        }
        //evaluamos si ya supero la cantidad de intentos
        if (cantidadIntentos==3){
            cantidadIntentos=0;
            lbIntentos.setText("No tiene mas intentos, vuelva a Generar.");
            btnGenerar.setEnabled(true);
            btnProbar.setEnabled(false);
            panelProbar.setVisible(false);
        }
        else{
            //sino creamos otro intento
            CrearIntento();
        }
        
    }//GEN-LAST:event_btnProbarActionPerformed

