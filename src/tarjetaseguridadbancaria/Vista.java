    //metodo del boton agregar
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        //creamos un objeto nuevo de la clase persona
        //agregamos los datos ingresados y validados por el usuario
        Persona persona=new Persona();
        persona.setIdentificacion(Integer.parseInt(txtIdentificacion.getText()));
        persona.setNombre(txtNombre.getText());
        persona.setApellido(txtApellido.getText());
        if (rbFemenino.isSelected()){persona.setSexo('F');}else{persona.setSexo('M');}
        persona.setFechanacimiento(dpFechaNacimiento.getDate());
        //calculamos la edad con respecto a la fecha de nacimiento
        persona.setEdadDiff(dpFechaNacimiento.getDate());
        
        //crear el modelo de la tabla de datos para la clase persona
        DefaultTableModel modeloPersona=(DefaultTableModel) jTablePersona.getModel();
        //se agrega la linea con los datos a la tabla
        modeloPersona.addRow(new Object[]{
            persona.getIdentificacion(),
            persona.getNombre(),
            persona.getApellido(),
            persona.getSexo(),
            persona.getFechanacimientoStrFormat(),
            persona.getEdad()
        });
        
        //crear un objeto cliente de la clase cliente
        //con los parametros del rango de numeros aleatorios para 
        //la tarjeta de seguridad
        Cliente cliente=new Cliente(10,99);
        //asignamos la identificacion al numero de cliente
        cliente.setNumerocliente(Integer.parseInt(txtIdentificacion.getText()));
        //creamos el arreglo bidimensional para la tarjeta de seguridad
        cliente.setNumerotarjeta();
        //llenamos el arreglo de la tarjeta de seguridad
        cliente.LlenaTarjeta();
        //agregamos el cliente a persona
        persona.setCliente(cliente);

        //crear el modelo de la tabla de datos para la clase cliente
        DefaultTableModel modeloCliente=(DefaultTableModel) jTableCliente.getModel();
        //se agrega la linea con los datos a la tabla
        modeloCliente.addRow(new Object[]{
            persona.getIdentificacion(),
            persona.getNombre(),
            persona.getApellido()
        });
        
        //agregamos el objeto persona a la lista personas
        listaPersona.add(persona);
        //limpiamos los campos
        txtIdentificacion.setText("");
        txtIdentificacion.requestFocus();
        txtNombre.setText("");
        txtApellido.setText("");
        
        //llamado al proceso para marcar los campos como requeridos
        formPersonaValidar();
        panelProbar.setVisible(false);
    }//GEN-LAST:event_btnAgregarActionPerformed

    //metodo para seleccion de fila en la tabla persona
    //carga la tarjeta de seguridad respectiva en la tabla
    private void jTablePersonaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablePersonaMouseClicked
        //variable con el indice del persona
        int indicePersona = jTablePersona.getSelectedRow();
        //crear el modelo de la tabla de datos para la clase tarjeta
        DefaultTableModel modeloTarjeta=(DefaultTableModel) jTableTarjeta.getModel();
        //removemos todos los datos existentes en la tabla
        modeloTarjeta.getDataVector().removeAllElements();
        modeloTarjeta.fireTableDataChanged();
        
        //buscamos el objeto persona en la lista
        Persona persona = listaPersona.get(indicePersona);
        //buscamos el objeto cliente del objeto persona
        Cliente cliente = persona.getCliente();
        
        //recorremos la tarjeta del objeto cliente
        //cargamos linea por linea en la tabla
        for (int i = 0; i < cliente.getNumerotarjeta().length; i++) {
            String[] row = new String[cliente.getNumerotarjeta()[i].length];
            for (int j = 0; j < cliente.getNumerotarjeta()[i].length; j++) {
                row[j]= String.valueOf(cliente.getNumerotarjeta()[i][j]);
            }
            modeloTarjeta.addRow(row);
        }
    }//GEN-LAST:event_jTablePersonaMouseClicked

